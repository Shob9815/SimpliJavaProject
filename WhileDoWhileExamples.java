package com.javaprograms;

public class WhileDoWhileExamples {
	
	public static void main(String args[])
	{
		//while loop -- here first we will check the condition and then increment and execute
		
		int i=0;
		
		while(i<3)
		{
			System.out.println("The value of I is ->" +i);
			i++;
		}
		
		//do while-- first execute and then chk the condition
		
		int a=0;
		
		do
		{
			System.out.println("The value of A->" +a);
			a++;
			
		}
		
		while(a<10);
		
		
		
		
			}

}
